public class SetEffect {

    // Fields:
    private String firstHalf;


    //------------------------------------------------------------------------------------------//

    // Constructor:
    public SetEffect() {
        this.firstHalf = "and your effect would be...";
    }

    //------------------------------------------------------------------------------------------//

    //
    public void extreme() {
        System.out.println(firstHalf + "Extreme.");
    }

    //------------------------------------------------------------------------------------------//

    //
    public void great() {
        System.out.println(firstHalf + "Great.");
    }

    //------------------------------------------------------------------------------------------//

    //
    public void standard() {
        System.out.println(firstHalf + "Standard.");
    }

    //------------------------------------------------------------------------------------------//

    //
    public void limited() {
        System.out.println(firstHalf + "Limited.");
    }

    //------------------------------------------------------------------------------------------//

    //
    public void none() {
        System.out.println(firstHalf + "None.");
    }
}
